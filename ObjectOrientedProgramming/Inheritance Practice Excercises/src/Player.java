public class Player {
    protected int playerNumber;
    protected String name;

    public Player() {
        this.playerNumber = playerNumber;
        this.name = name;
    }
    public int getPlayerNumber() {
        return playerNumber;
    }
    public void setPlayerNumber(int playerNumber) {
        this.playerNumber = playerNumber;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

}
