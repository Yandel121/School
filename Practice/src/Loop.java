public class Loop {
    public static void main(String[] args) {

        //while loop//
        int x=1;
                while(x<=1000){
                    System.out.println(x);
                    x=x+1;
                }

                //for loop//
                for (int i=1;i<=200;i=i+1){
                    System.out.println(i);
                }


                int countdown = 3;
        while (countdown > 0) {
            System.out.println(countdown);
            countdown--;
        }

        System.out.println("Happy New Year!!");
//Do while loop//
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }while(i<=100);


    }
}


